public class Tvod {

	private String offer_id;

	private String licence_duration;

	private String watch_window;

	private String complete_window;

	private String price;

	public String getOffer_id() {
		return offer_id;
	}

	public void setOffer_id(String offer_id) {
		this.offer_id = offer_id;
	}

	public String getLicence_duration() {
		return licence_duration;
	}

	public void setLicence_duration(String licence_duration) {
		this.licence_duration = licence_duration;
	}

	public String getWatch_window() {
		return watch_window;
	}

	public void setWatch_window(String watch_window) {
		this.watch_window = watch_window;
	}

	public String getComplete_window() {
		return complete_window;
	}

	public void setComplete_window(String complete_window) {
		this.complete_window = complete_window;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}
	
	
	
	

}
